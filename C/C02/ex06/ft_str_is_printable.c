int	ft_str_is_printable(char *str)
{
	if (*srt == '\0')
	{
		return (1);
	}
	while (*srt != '\0')
	{
		if (*srt >= 32 && *srt <= 126)
		{
		srt++;
		}
		else
			return (0);
	}
	return (1);
}
