char	*ft_strupcase(char *srt)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		if (srt[a] >= 'a' && str[a] <= 'z')
		{
			str[a] -= 32;
		}
		a++;
	}
	return (str);
}
